int	ft_sqrt(int nb)
{
	int	r;
	int	t;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	r = nb / 2;
	t = 0;
	while (r != t)
	{
		t = r;
		r = (nb / t + t) / 2;
		if (r > t)
			return (0);
	}
	if (r * r != nb)
		return (0);
	return (r);
}
